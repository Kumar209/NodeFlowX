"use client";

import { Button } from "@/components/ui/button";
import {
    Dialog,
    DialogContent,
    DialogDescription,
    DialogHeader,
    DialogTitle
} from "@/components/ui/dialog";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { CopyIcon } from "lucide-react";
import { useParams } from "next/navigation";
import { toast } from "sonner";
import { generateGoogleFormScript } from "./utils";


interface Props {
    open: boolean;
    onOpenChange: (open: boolean) => void;
};

export const GoogleFormTriggerDialog = ({
    open,
    onOpenChange
}: Props) => {
    const params = useParams();
    const workflowId = params.workflowId as string;

    //Construct the webhook URL
    const baseUrl = process.env.NEXT_PUBLIC_APP_URL || "http://localhost:3000";
    const webhookUrl = `${baseUrl}/api/webhooks/google-form?workflowId=${workflowId}`;

    const copyToClipboard = async () => {
        try{
            await navigator.clipboard.writeText(webhookUrl);
            toast.success("Webhook URL copied to clipboard");
        } catch {
            toast.error("Failed to copy URL");
        }
    }


    return (
        <Dialog open={open} onOpenChange={onOpenChange} >
            <DialogContent className="max-h-[85vh] overflow-hidden flex flex-col">
                <DialogHeader className="flex-shrink-0">
                    <DialogTitle>Google Form Trigger Configuration</DialogTitle>
                    <DialogDescription>
                        Use this webhook URL in your Google Form's Apps Script to trigger this workflow when a form is submitted.
                    </DialogDescription>
                </DialogHeader>

                <div 
                    className="flex-1 overflow-y-auto pr-4 space-y-6
                               [&::-webkit-scrollbar]:w-1
                               [&::-webkit-scrollbar-track]:bg-transparent
                               [&::-webkit-scrollbar-thumb]:bg-border
                               [&::-webkit-scrollbar-thumb]:rounded-full
                               [&::-webkit-scrollbar-thumb:hover]:bg-muted-foreground/50
                               [scrollbar-width]:thin
                               [scrollbar-color]:hsl(var(--border)/0.3)_transparent"
                >
                    <div className="space-y-4">
                        <div className="space-y-4">
                            <Label htmlFor="webhook-url">
                                Webhook URL
                            </Label>
                            <div className="flex gap-2">
                                <Input id="webhook-url" value={webhookUrl} readOnly className="font-mono text-sm" />
                                <Button type="button" size="icon" variant="outline" onClick={copyToClipboard} >
                                    <CopyIcon className="size-4" />
                                </Button>
                            </div>
                        </div>

                        <div className="rounded-lg bg-muted p-4 space-y-2">
                            <h4 className="font-medium text-sm">Setup instructions:</h4>
                            <ol className="text-sm text-muted-foreground-space-y-1 list-decimal list-inside">
                                <li>Open your Google Form</li>e
                                <li>Click the three dots menu → App Script</li>
                                <li>Copy and paste the script below</li>
                                <li>Replace WEBHOOK_URL with your webhook URL above</li>
                                <li>Save and click "Triggers" → Add Trigger</li>
                                <li>Choose: From form → On form submit → Save</li>
                            </ol>
                        </div>

                        <div className="rounded-lg bg-muted p-4 space-y-3">
                            <h4 className="font-medium text-sm" >Google Apps Script:</h4>
                            <Button
                            type="button"
                            variant="outline"
                            onClick={async () => {
                                const script = generateGoogleFormScript(webhookUrl);

                                try{
                                    await navigator.clipboard.writeText(script);
                                    toast.success("Script copied to clipboard");
                                } catch {
                                    toast.error("Failed to copy Script to clipboard");
                                }
                            }}
                            >
                                <CopyIcon className="size-4 mr-2" />
                            </Button>
                            <p className="text-xs text-muted-foreground">
                                This script includes your webhook URL and handles form submissions
                            </p>
                        </div>

                        <div className="rounded-lg bg-muted p-4 space-y-2">
                            <h4 className="font-medium text-sm">Available Variables</h4>
                            <ul className="text-sm text-muted-foreground space-y-1">
                                    {/* <li>
                                        <code className="bg-background px-1 py-0.5 rounded">
                                            {"{{googleForm.respondentEmail}}"}
                                        </code>
                                        - Respondent's email
                                    </li>
                                    <li>
                                        <code className="bg-background px-1 py-0.5 rounded">
                                            {"{{googleForm.responses['Question Name']}}"}
                                        </code>
                                        - Specific answer
                                    </li>
                                    <li>
                                        <code className="bg-background px-1 py-0.5 rounded">
                                            {"{{json googleForm.responses}}"}
                                        </code>{" "}
                                        - All responses as JSON
                                    </li> */}

                                <li>
                                    <code className="bg-background px-1 py-0.5 rounded">{"{{googleForm.respondentEmail}}"}</code>
                                    - Respondent's email address
                                </li>
                                <li>
                                    <code className="bg-background px-1 py-0.5 rounded">{"{{googleForm.responseId}}"}</code>
                                    - Unique response ID
                                </li>
                                <li>
                                    <code className="bg-background px-1 py-0.5 rounded">{"{{googleForm.responses.email}}"}</code>
                                    - Question named "email"
                                </li>
                                <li>
                                    <code className="bg-background px-1 py-0.5 rounded">{"{{googleForm.responses.['What is your name?']}}"}</code>
                                    - Question with spaces (bracket notation)
                                </li>
                                <li>
                                    <code className="bg-background px-1 py-0.5 rounded">{"{{json googleForm.responses}}"}</code>
                                    - All responses as formatted JSON
                                </li>
                                <li>
                                    <code className="bg-background px-1 py-0.5 rounded">{"{{json googleForm}}"}</code>
                                    - Complete form data as JSON
                                </li>
                                <li>
                                    <code className="bg-background px-1 py-0.5 rounded">{"{{todo.httpResponse.data.userId}}"}</code>
                                     - Nested data from previous nodes
                                </li>
                            </ul>
                        </div>

                    </div>
                </div>
            </DialogContent>
        </Dialog>
    )
}
